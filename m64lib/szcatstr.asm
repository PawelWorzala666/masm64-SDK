szCatStr proc src:QWORD, lpAdd:QWORD

    invoke szLen,src                ; get source length
    mov r10, src
    mov r11, r10
    mov rcx, lpAdd
    add r11, rax                    ; set write starting position
    xor rdx, rdx                    ; zero index

  @@:
    movzx rax, BYTE PTR [rcx+rdx]   ; write append string to end of source
    mov [r11+rdx], al
    add rdx, 1
    test rax, rax                   ; exit when terminator is written
    jne @B

    mov rax, r10                    ; return start address of source

    ret

szCatStr endp

