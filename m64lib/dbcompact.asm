dbcompact proc
  ; -------------------
  ; data address in RCX
  ; -------------------
    mov r11, rcx
    sub rcx, 1
    mov rdx, rcx

  align 16
  stlbl:
    add rcx, 1
    movzx rax, BYTE PTR [rcx]
    cmp BYTE PTR [rcx-1], "b"       ; only allow space after "db"
    je wrt
    cmp rax, 32
    je stlbl
  wrt:
    add rdx, 1
    mov [rdx], al
    test rax, rax
    jne stlbl

    sub rdx, r11
    mov rax, rdx                    ; return compacted written length

    ret

dbcompact endp

